# Дано ціле число N (> 0). Знайти добуток N! = 1 • 2 • ... • N (N-факторіал). Щоб уникнути
# цілочисельного переповнення, обчислювати цей добуток за допомогою дійсної змінної і вивести його
# як дійсне число.
n = float(input("enter the number = "))
if n <= 0:
    exit(0)
    print("Число повинно бути більшим за 0")


def factorial(n):
    fact = 1
    while n > 0:
        fact *= n
        n -= 1
    print(float(fact))


factorial(n)
